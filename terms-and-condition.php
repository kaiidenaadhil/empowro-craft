1. 🧾 Introduction

Empowro Craft is a digital marketplace and workflow platform connecting Customers, Designers, and Vendors for the creation, production, and delivery of printing and packaging services.

The platform ensures secure transactions, transparent workflow, and adherence to quality standards, providing both business efficiency and consumer protection.

By using this platform, all users agree to comply with these Terms & Conditions, which define legal obligations, operational rules, financial transactions, and dispute management.

2. 👥 User Roles & Responsibilities
2.1 Customer (গ্রাহক)

Customers are individuals or organizations who request printing or design services.
Responsibilities & Detailed Rules:

Information Accuracy: Customers must provide correct business or personal information, contact details, and delivery addresses. Incorrect information may cause delays or delivery failure.
Order Specifications: Customers must provide complete specifications, including:
Material type (GSM, paper type, etc.)
Colors (CMYK, Pantone, etc.)
Quantity, size, and finishing options
❗ Inaccurate specifications may lead to production errors for which the Vendor is not liable.
Proof Approval Responsibility: Customers must review proofs carefully. Once approved, the platform and Vendor are not responsible for errors in design or content.
Payment Obligation: Customers must make timely payments through the platform’s secure escrow system.
Cancellation & Refunds:
Before production: partial refund may be allowed.
After production starts: refunds are not allowed unless due to verified Vendor error.
2.2 Designer (ডিজাইনার)

Designers are professionals who create or modify designs based on Customer requirements.

Responsibilities & Rules:

Work Quality: All designs must be print-ready, CMYK compliant, with proper bleed and resolution. Files must adhere to standard printing specifications in Bangladesh.
Revision Management: Designers provide a defined number of revisions per project. Excess revisions may be charged.
Client Communication: Designers must respond promptly to messages or requests from Customers or Admin.
Intellectual Property (IP): Upon Customer approval, ownership of final designs transfers to the Customer. Designers cannot reuse or resell the designs without express permission.
Payment Release: Payments are released through the platform escrow system only after Customer approval or Admin validation.
2.3 Vendor / Printer (প্রিন্টিং প্রতিষ্ঠান)

Vendors are printing and production service providers.

Responsibilities & Rules:

Order Acceptance: Vendors must accept or reject incoming orders within the stipulated time. Delay or non-response may lead to reassignment.
Production Standards: Vendors must adhere to:
Material specifications (GSM, color type)
Color accuracy (allowing standard industry tolerances)
Finishing quality (lamination, cutting, binding)
Late Delivery (LD) Penalties: Delays beyond agreed SLA will result in penalties deducted from the payment.
Returns & Rework (RMA): If a major defect is found, Vendors are obligated to reprint or remake the order.
Capability Declaration: Vendors must declare their machinery, capabilities, and operational hours accurately. Misrepresentation may result in account suspension.
Financial Responsibility: Vendors must maintain accurate records for VAT and invoice generation as per Bangladesh law (Mushak 6.3/6.7).
2.4 Admin (প্ল্যাটফর্ম কর্তৃপক্ষ)

Admin represents Empowro Craft operations team.

Responsibilities & Authority:

Platform Control: Admin has the authority to monitor, override, and assign orders.
Dispute Resolution: Admin decisions are final regarding conflicts between Customers, Designers, and Vendors.
Escrow & Payment Management: Admin manages all funds in escrow and ensures compliance with commission structures.
Compliance Monitoring: Admin ensures all users adhere to platform rules, including SLA, quality, and legal obligations.
Audit & Security: Admin tracks all system actions via logs for accountability and regulatory compliance.
